package json;

public class KategorijaIzmena {
	public String staro;
	public String ime;
	public int broj;
	public int ram;
	public int gpu;
	
	public String getStaro() {
		return staro;
	}
	public void setStaro(String staro) {
		this.staro = staro;
	}
	public String getIme() {
		return ime;
	}
	public void setIme(String ime) {
		this.ime = ime;
	}
	public int getBroj() {
		return broj;
	}
	public void setBroj(int broj) {
		this.broj = broj;
	}
	public int getRam() {
		return ram;
	}
	public void setRam(int ram) {
		this.ram = ram;
	}
	public int getGpu() {
		return gpu;
	}
	public void setGpu(int gpu) {
		this.gpu = gpu;
	}
}
